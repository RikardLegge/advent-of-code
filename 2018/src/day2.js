export function day2a() {
  const words = input.split(/\s+/);
  const repeats = {};
  for(const word of words) {
    const letterCount = {};
    for(const letter of word) {
      letterCount[letter] = Number(letterCount[letter] || 0) + 1;
    }
    const uniqueCount = new Set(Object.values(letterCount));
    for(const count of uniqueCount.values()){
      repeats[count] = Number(repeats[count] || 0) + 1;
    }
  }
  return repeats[2] * repeats[3];
}

export function day2b() {
  const words = input.split(/\s+/);
  for(const word1 of words) {
    outerLoop: for(const word2 of words) {
      let difference = 0;
      let diffI;
      for(let i = 0; i < word2.length; i++) {
        if(word1[i] !== word2[i]) {
          difference++;
          diffI = i;
          if(difference > 1) continue outerLoop;
        }
      }
      if(difference === 1) {
        const hash = word1.slice(0, diffI) + word1.slice(diffI+1);
        return hash;
      }
    }
  }
}

